class Solution:
    def increasingTriplet(self, nums: List[int]) -> bool:
        first = float('inf')
        second = float('inf')

        for num in nums: #O(n)
            if num <= first:
                first = num
            elif num <= second:
                second = num
            else:
                return True
        return False

